#2.Variable Rules And Assignments
myvar=10
print(myvar)
Myvar=10
print(Myvar)
myvar1=10
print(myvar1)
_myvar=10
print(_myvar)
MYvar=10
print(MYvar)
#case sensitivity
myvar=10
print(myvar)
myvar=20
print(myvar)
#Multiple assignments
a,b,c=100,200,300
print(a)
print(b)
print(c)

#same value Assignment
a=b=c=100
print(a)
print(b)
print(c)

#swapping variable
a=100
b=50
a,b=b,a
print(a)
print(b)

#deleting asssignmemts
a=10
b=20
del (a)
print(b)